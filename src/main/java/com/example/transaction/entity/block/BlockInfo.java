package com.example.transaction.entity.block;

public class BlockInfo {
    private String blockIndex;
    private String currentBlockIndex;

    public String getBlockIndex() {
        return blockIndex;
    }

    public void setBlockIndex(String blockIndex) {
        this.blockIndex = blockIndex;
    }

    public String getCurrentBlockIndex() {
        return currentBlockIndex;
    }

    public void setCurrentBlockIndex(String currentBlockIndex) {
        this.currentBlockIndex = currentBlockIndex;
    }
} 